<h4>Physiotheraphy</h4>
<p>Physical therapy (PT), also known as physiotherapy, is one of the allied health professions that, by using evidence-based kinesiology, electrotherapy, shockwave modality, exercise prescription, joint mobilization and health education, treats conditions such as chronic or acute pain, soft tissue injuries, cartilage damage, arthritis, gait disorders and physical impairments typically of musculoskeletal, cardiopulmonary, neurological and endocrinological origins. Physical therapy is used to improve a patient's physical functions through physical examination, diagnosis, prognosis, physical intervention, rehabilitation and patient education.</p>
<br>
<h4>Cardiovascular and pulmonary physiotherapy</h4>
<p>Cardiovascular and pulmonary rehabilitation respiratory practitioners and physical therapists offer therapy for a wide variety of cardiopulmonary disorders or pre and post cardiac or pulmonary surgery. An example of cardiac surgery is coronary bypass surgery. Primary goals of this specialty include increasing endurance and functional independence. Manual therapy is used in this field to assist in clearing lung secretions experienced with cystic fibrosis. Pulmonary disorders, heart attacks, post coronary bypass surgery, chronic obstructive pulmonary disease, and pulmonary fibrosis, treatments can benefit[citation needed] from cardiovascular and pulmonary specialized physical therapists.</p>


